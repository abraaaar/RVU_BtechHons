public class LoopPractise {
    public static void main(String[] args) {
        System.out.println("Using For Loop");
        for (int i = 1; i <= 10; i++) {
            System.out.print(i+" ");
        }
        System.out.println("\n\nUsing while Loop");
        int num = 11;
        while(num <= 20){
            System.out.print(num+" ");
            num++;
        }
    }
}
